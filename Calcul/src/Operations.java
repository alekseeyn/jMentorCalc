public class Operations {

    public static int getPlus(int a, int b){
       int c = 0;
       c = a + b;
       return c;
    }

    public static int getMinus(int a, int b){
        int c = 0;
        c =  a - b;
        return c;
    }

    public static int getMulti(int a, int b){
        int c = 0;
        c = a * b;
        return c;
    }
    
    public static int getDevision(int a, int b){
        int c = 0;
        c = a / b;
        return c;
    }
}
